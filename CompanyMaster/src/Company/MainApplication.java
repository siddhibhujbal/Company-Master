     private void loadTableData(){
         try{
             String sql = "SELECT * FROM companies";
             PreparedStatement ps = conn.prepareStatement(sql);
              ResultSet rs = ps.executeQuery();
                DefaultTableModel model = new DefaultTableModel(
                 new String[]{"com_code", "name", "address", "phone", "website", "description", "contact_person", "contact_person_desg"}, 
                0);
                while (rs.next()) {
                     model.addRow(new Object[]{
                    rs.getString("com_code"),
                    rs.getString("name"),
                    rs.getString("address"),
                    rs.getString("phone"),
                    rs.getString("website"),
                    rs.getString("description"),
                    rs.getString("contact_person"),
                    rs.getString("contact_person_desg")
                });
                }
                 tblcompany.setModel(model);
         }
         catch(SQLException e){
             JOptionPane.showMessageDialog(this, "Error loading table data: " + e.getMessage());
         }
     }
    
    

