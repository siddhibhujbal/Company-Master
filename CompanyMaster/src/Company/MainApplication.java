    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        try{
             String sql = "UPDATE companies SET name=?, address=?, phone=?, website=?, description=?, contact_person=?, contact_person_desg=? WHERE com_code=?";
             PreparedStatement ps = conn.prepareStatement(sql);
              ps.setString(1, txtname.getText());
            ps.setString(2, taaddress.getText());
            ps.setString(3, txtphone.getText());
            ps.setString(4, txtwebsite.getText());
            ps.setString(5, tadesc.getText());
            ps.setString(6, txtcontactperson.getText());
            ps.setString(7, txtcontactpersondesc.getText());
            ps.setString(8, txtcode.getText());
            int rowsUpdated = ps.executeUpdate();
              if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Record updated successfully!");
                loadTableData(); // Refresh JTable data
            } else {
                JOptionPane.showMessageDialog(this, "Record not found!");
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(this, "Error updating record: " + e.getMessage());
        }
            
    }//GEN-LAST:event_btneditActionPerformed

